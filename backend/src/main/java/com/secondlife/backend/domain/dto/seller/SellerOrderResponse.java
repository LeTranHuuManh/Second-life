package com.secondlife.backend.domain.dto.seller;

import com.secondlife.backend.domain.enums.OrderStatus;
import java.math.BigDecimal;
import java.time.LocalDateTime;

public class SellerOrderResponse {
    private Long orderItemId; // The ID of the individual order item
    private Long orderId; // The ID of the parent CustomerOrder
    private String customerName; // The buyer's name
    private Long productId;
    private String productName;
    private String productImageUrl;
    private String itemType; // BUY or RENT
    private BigDecimal price; // Total for this item depending on qty/rent days
    private LocalDateTime orderDate;
    private OrderStatus status; // Inherited from CustomerOrder

    public Long getOrderItemId() {
        return orderItemId;
    }

    public void setOrderItemId(Long orderItemId) {
        this.orderItemId = orderItemId;
    }

    public Long getOrderId() {
        return orderId;
    }

    public void setOrderId(Long orderId) {
        this.orderId = orderId;
    }

    public String getCustomerName() {
        return customerName;
    }

    public void setCustomerName(String customerName) {
        this.customerName = customerName;
    }

    public Long getProductId() {
        return productId;
    }

    public void setProductId(Long productId) {
        this.productId = productId;
    }

    public String getProductName() {
        return productName;
    }

    public void setProductName(String productName) {
        this.productName = productName;
    }

    public String getProductImageUrl() {
        return productImageUrl;
    }

    public void setProductImageUrl(String productImageUrl) {
        this.productImageUrl = productImageUrl;
    }

    public String getItemType() {
        return itemType;
    }

    public void setItemType(String itemType) {
        this.itemType = itemType;
    }

    public BigDecimal getPrice() {
        return price;
    }

    public void setPrice(BigDecimal price) {
        this.price = price;
    }

    public LocalDateTime getOrderDate() {
        return orderDate;
    }

    public void setOrderDate(LocalDateTime orderDate) {
        this.orderDate = orderDate;
    }

    public OrderStatus getStatus() {
        return status;
    }

    public void setStatus(OrderStatus status) {
        this.status = status;
    }
}
